      <div class="down">
        <div class="rong"> <p>
	　　呼和浩特都市丽人医院是经上级卫生部门批准设立的集医疗、预防、科研、保健于一体的医疗保健机构，全区首家精细化、专业化妇科医院。</p>
<p>
	　　医院致力于妇科疾病的研究和治疗，现已设立了妇科专家会诊中心，外科腔镜治疗中心、无痛人流服务中心、妇科整形中心及妇科炎症、乳腺、子宫肌瘤、宫颈糜烂、不孕不育、内分泌、计划生育、少女门诊、预防保健、心理咨询等特色专科。</p>
<p>
	　　呼和浩特都市丽人医院坚持以&ldquo;专科医疗、专病专治、特色服务、特色经营&rdquo;两条路线，首推&ldquo;用心倾听、细心诊断、耐心解答、精心治疗、热心服务、全心呵护、真心奉献、衷心祝福&rdquo;的八&ldquo;心&rdquo;级服务理念，开创&ldquo;无假日医院&rdquo;&ldquo;全程免费导医&rdquo;&ldquo;七天专家门诊制&rdquo;的全新服务模式，是一个专为患者考虑的&ldquo;无红包、无回扣&rdquo;医院。针对患者个体差异制定一对一诊疗方案，确保临床疗效，倡导&ldquo;以人为本、追求卓越&rdquo;的经营模式 。关注人性化服务，遵循&ldquo;以病人需求为中心，以病人满意为目标&rdquo;的服务宗旨。医院全面实行计算机网络管理，有医院专属网站，同时开设远程会诊中心，与众多国内著名医疗机构建立长期的合作关系，时刻掌握医疗技术的最新动态，为疾病的预防及治疗打下坚实的基础。</p>
<p>
	　　<strong>医院环境</strong></p>
<p>
	　　医院位于呼和浩特回民区，环境宜人，交通便利。医疗楼设施高档、设备先进。医院秉承&ldquo;以人为本&rdquo;的理念，结合医院幽雅的就医环境，为患者提过私密诊疗、质量控制、全程陪护等宾馆式享受的星级服务。</p>
<p>
	　　<strong>专业妇科</strong></p>
<p>
	　　医院以建立&ldquo;专家型、学者型、创新性&rdquo;妇科名院为目标，聘请了大批医疗及管理尖端人才，以国内知名的妇科专家、教授等为科学带头人，并与北京、上海等国内外知名医院建立协作关系，将呼市乃至全区的妇科诊疗水平提上一个新台阶。</p>
<p>
	　　呼和浩特都市丽人医院(呼和浩特市无痛人流援助中心)秉承专业、精细、卓越品质，以高度尊重女性生命质量的态度提供医疗服务。医院自建院以来，就致力于打造成为&ldquo;内蒙古自治区第一妇科品牌医院&rdquo;。经过不懈的努力，呼市都市丽人医院已成长为省内具有雄厚技术师资实力、专业设备齐全、享有市民良好口碑的妇科专科医院。</p>
<p>
	　　<strong>专业技术</strong></p>
<p>
	　　医院拥有梦之流国际无痛人流术、美国爱尔荣LEEP刀、美国GE彩超、CDYD低频共振磁导疗法治疗仪、不孕症诊疗治疗仪、WOLF腹腔镜、TCT细胞学检测仪、XG-5宫腔镜、体外短波CRS电容场热疗系统、迈瑞BS200全自动生化分析仪、日本奥林巴斯数码电子阴道镜等国际尖端的诊疗设备。形成了内蒙古规模宏大、类别齐全、设备先进、能进行全方位诊断和治疗各类妇科疾病及妇科整形的医疗机构。</p>
<p>
	　　<strong>医院理念</strong></p>
<p>
	　　医院以建立&ldquo;专家型、学者型、创新性&rdquo;妇科名院为目标，聘请了大批医疗及管理尖端人才，以国内知名的妇科专家、教授等为科学带头人，并与北京、上海等国内外知名医院建立协作关系，将呼市乃至全区的妇科诊疗水平提上一个新台阶。</p>
<p>
	　　医院以&ldquo;一切服务于病人，一切以病人为中心，一切为了病人&rdquo;为服务理念;&ldquo;以建设技术一流、设备一流、服务一流和管理一流的百姓满意医院&rdquo;为建院目标;&ldquo;以一切为了病人，全心全意为病人服务&rdquo;为建院宗旨。立志打造&ldquo;现代都市女性健康会所&rdquo;，突破人们对&ldquo;医院&rdquo;概念的固有理解, 引领都市女性现代化妇科专科医院潮流。</p>
<p>
	　　<strong>服务承诺</strong></p>
<p>
	　　一、自觉遵守国家法律、法规和规章。规范医院医疗行为，严格自律，打造&ldquo;诚信品牌&rdquo;，构建和谐医患关系典范。</p>
<p>
	　　二、保证做到&ldquo;五个心&rdquo;：工作用心、检查细心、护理精心、诊疗耐心、服务热心。</p>
<p>
	　　&ldquo;五不准&rdquo;：不准以任何借口推诿病人，延误抢救时机;不准在任何时刻、任何场合与患者或患者家属发生争吵;不准索取病人财物，接受病人红包;不准对患者解答轻率马虎。</p>
<p>
	　　三、简化就医流程，缩短候诊时间。实行&ldquo;无假日医院&rdquo;。推行节假日、双休日、24小时专家门诊。开设&ldquo;绿色生命通道&rdquo;。以人为本，从管理上体现诚信，从管理上体现诚信，从制度上规范诚信。</p>
<p>
	　　四、根据患者病情，用心诊疗，规范用药。</p>
<p>
	　　五、保护患者隐私。尊重患者的知情权、选择权和监督权。</p>
<p>
	　　六、价格公开、规范收费。收费项目公示，100%实行门诊收费精准账单制和住院费用&ldquo;一日清&rdquo;制。</p>
<p>
	　　七、24小时开通健康咨询热线，方便患者咨询问诊。</p>
<p>
	　　八、用药安全，保证质量。严禁使用过期失效药品，严格执行发药查对和告知制度。</p>
<p>
	　　<strong>网络预约</strong></p>
<p>
	　　呼和浩特都市丽人医院坚决维护个人隐私权，网上预约为您的就诊提供方便。网上预约成功后，医院将根据患者信息安排相关专家一对一就诊。同时医院专家提醒在线预约的患者，为了方便自己，也方便他人，一定要按照预约时间准时就诊。</p>
<p>
